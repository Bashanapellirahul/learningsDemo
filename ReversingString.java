package pkg1;

public class ReversingString {

	public static void main(String[] args) {
		

		String name = "rahul";
		
		int num []= {1,2,3,4,5};
		
		for(int j=num.length-1;j>=0;j--) {		//reversing numbers
			
			System.out.print(num[j]);
			System.out.println();
			
		}
			
		
		System.out.println("=========");
			//reversing a string
		
		for(int i = name.length()-1;i>=0;i--) {
			
			System.out.println(name.charAt(i));
	
}
		System.out.println("=========");
		int a;
				for(a=5;a>=1;a--) {
					System.out.print(a);	//reversing numbers
					
				}
				System.out.println();
				System.out.println("=========");
				
			}

}
